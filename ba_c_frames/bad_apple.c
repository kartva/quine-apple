float eval_sdf(float x, float y, Primitive* prims, int count) {
    float d = 1e9f;
    for (int i = 0; i < count; ++i) {
        Primitive p = prims[i];
        float dist = 1e9f;
        if (p.type == CIRCLE) {
            float dx = x - p.x, dy = y - p.y;
            dist = sqrtf(dx*dx + dy*dy) - p.w;
        } else {
            float dx = fabsf(x - p.x) - p.w;
            float dy = fabsf(y - p.y) - p.h;
            float ax = dx > 0.0f ? dx : 0.0f;
            float ay = dy > 0.0f ? dy : 0.0f;
            dist = sqrtf(ax*ax + ay*ay);
            if (dx <= 0.0f && dy <= 0.0f) dist = fmaxf(dx, dy);
        }
        if (dist < d) d = dist;
    }
    return d;
}

char map_to_ascii(float d) {
    float t = 1.0f - d * SDF_SCALE;
    if (t < 0.0f) t = 0.0f;
    if (t > 1.0f) t = 1.0f;
    const char* gradient = " .:-=+*#%@";
    int levels = 9;
    int idx = (int)(t * levels);
    if (idx < 0) idx = 0;
    if (idx > levels) idx = levels;
    return gradient[idx];
}

